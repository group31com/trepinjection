			<h4>ADVERSE REACTIONS</h4>
			<p>During clinical trials with SC infusion of treprostinil, infusion site pain and infusion site reaction (eg, redness of the skin, hardness of the skin, or rash) were the most common adverse events and occurred in the majority of those treated with treprostinil. Infusion site reactions were sometimes severe and led to discontinuation of treatment. Rash and low blood pressure (14% and 4%, respectively) were also commonly reported with SC infusion of treprostinil. Other common adverse events (â‰¥9% of patients in the treprostinil arm) with either SC or IV treprostinil included headache, diarrhea, jaw pain, edema (swelling), vasodilatation (widening of the blood vessels), and nausea. The adverse reactions with treprostinil IV included bloodstream infections, arm swelling, tingling or prickling sensation, bruising, and pain.</p>
			<h4>DRUG INTERACTIONS</h4>
