			<h3>INDICATION</h3>
			<p>Treprostinil Injection is a prescription medication used in adults with pulmonary arterial hypertension (PAH), World Health Organization (WHO) Group 1, to reduce symptoms associated with exercise. PAH is high blood pressure in the arteries of your lungs. Treprostinil was studied mainly in patients with New York Heart Association (NYHA) Functional Class II-IV symptoms.</p>
			<p>In people with PAH who need to switch from epoprostenol sodium, Treprostinil Injection is approved to slow the worsening of symptoms. The risks and benefits of each drug should be carefully considered before switching.</p>
			<h3 id="isi">IMPORTANT SAFETY INFORMATION</h3>
			<h4>CONTRAINDICATIONS</h4>
			<p>None.</p>
			<h4>WARNINGS AND PRECAUTIONS</h4>
			<div class="warning-bullets">
				<table>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> Treprostinil Injection can be delivered using a tube placed in a big blood vessel (vein) within the chest. This type of delivery is associated with the risk of bloodstream infections and sepsis, which may be fatal. Therefore, continuous subcutaneous infusion delivered just beneath the skin is the preferred method of delivery.</td>
					</tr>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> You should not stop or change your Treprostinil Injection dose without consulting your doctor first, as this may cause your PAH symptoms to worsen.</td>
					</tr>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> Treprostinil Injection acts by lowering your blood pressure. In some cases, your blood pressure may become too low and cause other side effects. If you also take other drugs that lower your blood pressure, the risk is increased. You should discuss all of your symptoms with your doctor, including those associated with low blood pressure.</td>
					</tr>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> If you have liver or kidney problems, your ability to tolerate Treprostinil Injection may be affected.</td>
					</tr>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> Because Treprostinil Injection can reduce the blood's ability to clot, it may increase your risk of bleeding, especially if you are taking anticoagulants (blood thinners).</td>
					</tr>
					<tr>
						<td style="vertical-align: top; color: #22647e;">&bull;&nbsp;</td>
						<td> Other medical conditions and medicines may affect your use of Treprostinil Injection by increasing the risk of side effects or decreasing the drug's effectiveness. It is important to tell your doctor about your medical conditions and any medicines you may be taking.</td>
					</tr>
